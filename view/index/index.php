       <b> Key terms </b></br>
       E-government adoption is about government providing services using ICT infrastructure 
        in the country /organisation. E-government lifecycle comprises of the following phases:</br>
        <b> Digital  presence</b></br>
         In this stage government organisations will be expected to be providing one-way 
         information and limited interaction to their clients.</br>
         <b>Interaction</b></br> 
         Government use ICT to provide
         some degree of online interaction with their clients. For instance, citizens can be able to 
         enter requests, complaints, or job applications online, and expect to obtain an appropriate 
         response.</br>
          <b>Transaction </b></br>
         This stage is characterized by the provision of secure transactions 
         with high level of authorization. Government organisations are expected to be able to provide 
         capabilities and features that will allow clients to complete their transactions in full 
         without the necessity of visiting government offices. Such services may also allow the 
         government to function in a 24/7 mode.</br>
         <b> Transformation</b></br>
          In this stage government organisations 
         are expected to have been well joined and working together at all levels. The achieving of 
         this stage will allow government clients to interact with one Government instead of individual
          Government organisations. This phase require collaboration to bring together suppliers, 
          consumers and the whole government itself into a seamless network focused on increasing 
          value creation.
